#include <iostream>
using namespace std;

int main() {
    cout << "=== Operator Aritmatika ===\n";
    cout << "5 + 3 = " << 5 + 3 << endl;
    cout << "5 - 3 = " << 5 - 3 << endl;
    cout << "5 * 3 = " << 5 * 3 << endl;
    cout << "10 / 2 = " << 10 / 2 << endl;
    cout << "10 % 3 = " << 10 % 3 << endl;

    return 0;
}
