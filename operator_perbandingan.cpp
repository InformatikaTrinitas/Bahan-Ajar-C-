#include <iostream>
using namespace std;

int main() {
    cout << "=== Operator Perbandingan ===\n";
    cout << "5 == 5 -> " << (5 == 5) << endl;
    cout << "5 != 3 -> " << (5 != 3) << endl;
    cout << "5 > 3  -> " << (5 > 3) << endl;
    cout << "3 < 5  -> " << (3 < 5) << endl;
    cout << "5 >= 5 -> " << (5 >= 5) << endl;
    cout << "3 <= 5 -> " << (3 <= 5) << endl;

    return 0;
}
